/**
 * Fetch Rinus SVG URLs, spelregels and display metadata for all mapped exercise ids.
 * Run: node scripts/sync-rinus-content.mjs
 */
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'
import { RINUS_ID_MAP } from '../src/data/rinusLinks.js'
import { EXERCISES } from '../src/data/exercises.js'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const svgOut = path.join(__dirname, '../src/data/rinusSvgMap.js')
const rulesOut = path.join(__dirname, '../src/data/rinusRulesMap.js')
const metaOut = path.join(__dirname, '../src/data/rinusMetaMap.js')

function parseEntry(entry) {
  const content = entry?.exerciseContent?.[0]
  const activity = entry?.exerciseActivityDefaultContentItem?.[0]
  if (!content) return null

  const svg = content.exerciseFigure?.[0]?.exerciseSvgImage?.[0]?.url ?? null
  const rules = (content.exerciseRules ?? [])
    .map(r => r.title?.trim())
    .filter(Boolean)
  const dimensions = (content.exerciseDimensions ?? [])
    .map(d => d.title?.trim())
    .filter(Boolean)

  return {
    title: content.exerciseTitle?.trim() ?? null,
    durationMin: content.exerciseDuration ?? null,
    minPlayers: content.playersMinimum ?? null,
    maxPlayers: content.playersMaximum ?? null,
    dimensions,
    description: activity?.activityExplanation?.trim() ?? null,
    footballAction: activity?.activityCategory?.[0]?.title?.trim() ?? null,
    svg,
    rules,
  }
}

async function fetchRinusContent(rinusId) {
  const res = await fetch(`https://rinus.knvb.nl/nl/exercise/id/${rinusId}`)
  if (!res.ok) return null
  const html = await res.text()
  const nextMatch = html.match(/<script id="__NEXT_DATA__"[^>]*>([\s\S]*?)<\/script>/)
  if (!nextMatch) return null
  const entry = JSON.parse(nextMatch[1])?.props?.pageProps?.entry
  return parseEntry(entry)
}

const uniqueIds = [...new Set(Object.values(RINUS_ID_MAP))]
const contentByRinusId = {}

for (const id of uniqueIds) {
  process.stdout.write(`Fetching ${id}... `)
  contentByRinusId[id] = await fetchRinusContent(id)
  const c = contentByRinusId[id]
  console.log(c ? `${c.title} (${c.rules.length} rules)` : 'missing')
  await new Promise(r => setTimeout(r, 150))
}

const svgByRinusId = {}
const rulesByRinusId = {}
const metaByRinusId = {}

for (const [id, content] of Object.entries(contentByRinusId)) {
  if (!content) continue
  svgByRinusId[id] = content.svg
  rulesByRinusId[id] = content.rules
  metaByRinusId[id] = {
    title: content.title,
    durationMin: content.durationMin,
    minPlayers: content.minPlayers,
    maxPlayers: content.maxPlayers,
    dimensions: content.dimensions,
    description: content.description,
    footballAction: content.footballAction,
  }
}

const svgMap = {}
const rulesMap = {}
const metaMap = {}

for (const ex of EXERCISES) {
  const rinusId = ex.rinusId ?? RINUS_ID_MAP[ex.id]
  const content = rinusId ? contentByRinusId[rinusId] : null
  svgMap[ex.id] = content?.svg ?? null
  rulesMap[ex.id] = content?.rules ?? []
  metaMap[ex.id] = rinusId && metaByRinusId[rinusId] ? { ...metaByRinusId[rinusId] } : null
}

fs.writeFileSync(svgOut, `/**
 * Rinus exercise diagram SVG URLs (CDN).
 * Generated by scripts/sync-rinus-content.mjs
 */
export const RINUS_SVG_BY_RINUS_ID = ${JSON.stringify(svgByRinusId, null, 2)}

export const RINUS_SVG_MAP = ${JSON.stringify(svgMap, null, 2)}
`)

fs.writeFileSync(rulesOut, `/**
 * Rinus spelregels per oefening.
 * Generated by scripts/sync-rinus-content.mjs
 */
export const RINUS_RULES_BY_RINUS_ID = ${JSON.stringify(rulesByRinusId, null, 2)}

export const RINUS_RULES_MAP = ${JSON.stringify(rulesMap, null, 2)}
`)

fs.writeFileSync(metaOut, `/**
 * Rinus display metadata (title, description, dimensions, …).
 * Generated by scripts/sync-rinus-content.mjs
 */
export const RINUS_META_BY_RINUS_ID = ${JSON.stringify(metaByRinusId, null, 2)}

export const RINUS_META_MAP = ${JSON.stringify(metaMap, null, 2)}
`)

console.log('Wrote', svgOut, rulesOut, metaOut)
